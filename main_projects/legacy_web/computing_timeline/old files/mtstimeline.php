          <h4>1966</h4>
          <ul>
            <li>May 1966
				<ul>
            		<li>IBM 360 Model 50, located at the North University Building (NUBS), used
            		for early MTS development work.</li>
				</ul>
			</li>
            
			<li>1966
				<ul>
					<li>Merit Network founded.</li>
					<li>First Data Concentrator built (as part of the ConComp Project). For more
					than 10 years, these provided the bulk of the access to MTS for timesharing
					users.</li>
				</ul>
			</li>
          </ul>
          <h4>1967-1968</h4>
          <ul>
            <li>January 1967
		  		<ul>
            		<li>Computing Center obtains a new IBM 360 Model 67.</li>
            	</ul>
			</li>
			<li>May 1967
				<ul>
            		<li>MTS released to campus as operating system for IBM 360/67.</li>
				</ul>
			</li>
			<li>1967
				<ul>
            		<li>Batch-mode access and billing for CPU time added to MTS.</li>
				</ul>
			<li>February 1968
				<ul>
            		<li>First remote graphics station connected to MTS.</li>
				</ul>
			</li>
            <li>August 1968
				<ul>
            		<li>Computing Center obtains dual-processor 360/67.</li>
				</ul>
			</li>
			<li>November 1968
				<ul>
            		<li>University of British Columbia runs MTS.</li>
				</ul>
			</li>
          </ul>
          <h4>1969-1973</h4>
          <ul>
            <li>February 1969
				<ul>
            		<li>University of Newcastle runs MTS.</li>
				</ul>
			</li>
            <li>October 1969
				<ul>
            		<li>Construction commences on North Campus Computing Center Building.</li>
				</ul>
			</li>
            <li>September 1970
				<ul>
            		<li>University of Alberta runs MTS.</li>
				</ul>
			</li>
            <li>January 1971
				<ul>
            		<li>Wayne State University runs MTS.</li>
				</ul>
			</li>
            <li>April 1971
				<ul>
            		<li>Computing Center moves to new building; NUBS remains as a remote batch
            station. MTS runs on an Amdahl 470V/6 computer (serial number 2).</li>
				</ul>
			</li>
          </ul>
          
		  <h4>1974-1980</h4>
          <ul>
            <li>May 1974
				<ul>
					<li>First MTS workshop held at University of British Columbia.</li>
				</ul>
			</li>
			<li>1975
				<ul>
           			<li>Confer software provides conferencing and e-mail services via MTS.</li>
				</ul>
			</li>
            <li>1976
				<ul>
            		<li>"Which Host?" network prompt first appears.</li>
				</ul>
			</li>
            <li>July 1976
				<ul>
					<li>Rensselaer Polytechnic University runs MTS.</li>
				</ul>
			</li>
            <li>October 1976
				<ul>
            		<li>Confer and MTS used in support of a conference of the International
            		Society for Technology Assessment.</li>
				</ul>
			</li>
          </ul>
          
		  <h4>1981-1984</h4>
          <ul>
            <li>1981
				<ul>
					<li>Microcomputer access to MTS provided.</li>
				</ul>
			</li>
            <li>July 1981
				<ul>
            		<li>$MESSAGE e-mail available on MTS.</li>
				</ul>
			</li>
            <li>September 1982
				<ul>
            		<li>MTS able to send and receive e-mail to and from remote, non-MTS
            		systems.</li>
				</ul>
			</li>
            <li>1983
				<ul>
            		<li>First Secondary Communications Processor (SCP) deployed.</li>
				</ul>
			</li>
            <li>1984
				<ul>
            		<li>Information Technology Division formed by combining Computing Center,
            		Office of Administrative Systems, and U-M Office of Telecommunications.</li>
				</ul>
			</li>
          </ul>
          <h4>1985-1996</h4>
          <ul>
            <li>May 1985
				<ul>
            		<li>UB system and request accounts available to the campus community.</li>
				</ul>
			</li>
            <li>January 1986
				<ul>
            		<li>The first U-M computer conference organized by students (Meet:students)
            		is opened to all students using Confer on MTS.</li>
				</ul>
			</li>
            <li>January 1992
				<ul>
            		<li>UB system merged into UM system.</li>
				</ul>
			</li>
            <li>April 1996
				<ul>
            		<li>More than 49 million messages sent since 1981 using e-mail on MTS.</li>
				</ul>
			</li>
            <li>June 1996
				<ul>
            		<li>MTS retired at U-M. Still running at Wayne State University, Rensselaer
            	Polytechnic University, and the University of British Columbia.</li>
				</ul>
			</li>
          </ul>

		
